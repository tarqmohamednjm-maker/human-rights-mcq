const LETTERS = ['أ','ب','ج','د'];
let deck = [...QUESTIONS];
let idx = 0;
let answers = new Array(QUESTIONS.length).fill(null);
let answered = new Array(QUESTIONS.length).fill(false);

function shuffleAll() {
  for (let i = deck.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [deck[i], deck[j]] = [deck[j], deck[i]];
  }
  answers = new Array(deck.length).fill(null);
  answered = new Array(deck.length).fill(false);
  idx = 0;
  render();
}

function go(dir) {
  idx = Math.max(0, Math.min(deck.length - 1, idx + dir));
  render();
}

function pick(qi, oi) {
  if (answered[qi]) return;
  answers[qi] = oi;
  answered[qi] = true;
  render();
}

function score() {
  return deck.reduce((s, q, i) => s + (answers[i] === q.ans ? 1 : 0), 0);
}

function render() {
  const q = deck[idx];
  const isAnswered = answered[idx];
  const chosen = answers[idx];
  const total = deck.length;
  const done = answered.filter(Boolean).length;

  document.getElementById('qmeta').textContent = 'سؤال ' + (idx+1) + ' من ' + total + ' | تمت الإجابة: ' + done;
  document.getElementById('qprog').style.width = Math.round((idx+1)/total*100) + '%';
  document.getElementById('prev-btn').disabled = idx === 0;
  document.getElementById('next-btn').disabled = idx === deck.length - 1;

  let optsHTML = q.opts.map((o, oi) => {
    let cls = 'opt';
    if (isAnswered) {
      if (oi === q.ans) cls += ' correct';
      else if (oi === chosen) cls += ' wrong';
    }
    return `<div class="${cls}" onclick="pick(${idx},${oi})">
      <span class="opt-letter">${LETTERS[oi]}</span>
      <span>${o}</span>
    </div>`;
  }).join('');

  let fb = '';
  if (isAnswered) {
    if (chosen === q.ans) {
      fb = `<div class="feedback ok">إجابة صحيحة!</div>`;
    } else {
      fb = `<div class="feedback no">خطأ. الإجابة الصحيحة: ${q.opts[q.ans]}</div>`;
    }
  }

  const allDone = answered.every(Boolean);
  const sc = score();
  const pct = Math.round(sc/total*100);

  let summary = '';
  if (allDone) {
    let grade = pct >= 90 ? 'ممتاز!' : pct >= 75 ? 'جيد جداً' : pct >= 60 ? 'جيد' : 'تحتاج إلى مراجعة';
    summary = `<div class="score-box">
      <div class="score-num">${sc} / ${total}</div>
      <div class="score-pct">${pct}%</div>
      <div class="score-lbl">${grade}</div>
      <button class="qbtn" style="margin-top:2rem; background: var(--color-primary); color: white;" onclick="shuffleAll()">إعادة الاختبار</button>
    </div>`;
  }

  document.getElementById('quiz-area').innerHTML = `
    <div class="qcard">
      <div class="qnum">
        <span class="tag ${q.type==='tf'?'tag-tf':'tag-mc'}">${q.type==='tf'?'صح / خطأ':'اختيار من متعدد'}</span>
      </div>
      <div class="qtext">${q.q}</div>
      ${optsHTML}
      ${fb}
    </div>
    ${summary}
  `;
}

render();